const express = require("express");
const auth = require("../middleware/auth");
const multer = require("multer");
const sharp = require("sharp");
const Customer = require("../models/customer");
const Pet = require("../models/pet");
const customerAuth = require("../middleware/customerAuth");


const router = express.Router();

//login as customer
router.post("/asCustomer/login", async (req, res) => {
    try {
        const customer = await Customer.findByCredentials(req.body.email, req.body.password);
        const token = await customer.generateAuthToken();
        res.send({ customer: customer, token });
    } catch (e) {
        res.status(400).send();
    }
});

//get profile as customer
router.get("/asCustomers/me", customerAuth, async (req, res) => {
    res.send(req.customer);
});

//get all pets as customer
router.get("/asCustomer/pets", customerAuth, async (req, res) => {
    try {
        await req.customer
            .populate({
                path: "pets",
            })
            .execPopulate();
        res.send(req.customer.pets);
    } catch (e) {
        res.status(500).send();
    }
});

//Read single pet as customer
router.get("/asCustomer/pet/:id", customerAuth, async (req, res) => {
    const _id = req.params.id;

    try {
        const pet = await Pet.findOne({ _id, owner: req.customer._id });
        if (!pet) {
            return res.status(404).send();
        }

        res.send(pet);
    } catch (e) {
        res.status(500).send();
    }
});

//logout as customer
router.post("/asCustomers/logout", customerAuth, async (req, res) => {
    try {
        req.customer.tokens = req.customer.tokens.filter((token) => token.token != req.token);
        await req.customer.save();
        res.send();
    } catch (e) {
        res.status(500).send();
    }
});

//Add a new customer
router.post("/user/addcustomer", auth, async (req, res) => {
    const customer = new Customer({
        ...req.body,
        vet: req.user._id,
    });
    try {
        await customer.save();
        const token = await customer.generateAuthToken();
        res.status(201).send({ customer, token });
    } catch (e) {
        res.status(500).send(e);
    }
});

//logout from everywhere as customer
router.post("/asCustomer/logoutAll", customerAuth, async (req, res) => {
    try {
        req.customer.tokens = [];
        await req.customer.save();
        res.send();
    } catch (e) {
        res.status(500).send();
    }
});

//Get all customers
router.get("/customers", auth, async (req, res) => {
    try {
        await req.user
            .populate({
                path: "customers",
            })
            .execPopulate();
        res.send(req.user.customers);
    } catch (e) {
        res.status(500).send();
    }
});

//Read a customer
router.get("/customers/:id", auth, async (req, res) => {
    const _id = req.params.id;
    try {
        const customer = await Customer.findOne({ _id, vet: req.user._id });
        if (!customer) res.status(400).send();
        res.send(customer);
    } catch (e) {
        res.status(500).send();
    }
});

router.patch("/customers/:id", auth, async (req, res) => {
    const updates = Object.keys(req.body);
    const allowedUpdates = ["username","email","password"];

    const isValidOperation = updates.every((update) => allowedUpdates.includes(update))
    if(!isValidOperation) res.status(400).send({error: "Invalid updates"})

    try {
        
        const customer = await Customer.findOne({ _id: req.params.id, vet: req.user });

        if (!customer) res.status(404).send();

        updates.forEach((update) => {
            customer[update] = req.body[update];
        });
        await customer.save();
        res.send(customer);
    } catch (e) {
        res.status(400).send('Invalid updates');
    }
});

router.delete("/customers/:id", auth, async (req, res) => {
    try {
        const customer = await Customer.findOneAndDelete({ _id: req.params.id, vet: req.user._id });
        if (!customer) res.status(400).send();
        res.send(customer);
    } catch (e) {
        res.status(500).send();
    }
});

const upload = multer({
    limits: {
        fileSize: 3000000,
    },
    fileFilter(req, file, cb) {
        if (!file.originalname.match(/\.(jpg|jpeg|png)$/)) {
            return cb(new Error("Please upload a picture"));
        }
        return cb(undefined, true);
    },
});

router.post(
    "/pets/:id/avatar",
    customerAuth,
    upload.single("avatar"),
    async (req, res) => {
        const buffer = await sharp(req.file.buffer).resize({ width: 250, height: 250 }).png().toBuffer();

        const pet = await Pet.findById(req.params.id);
        if (!pet) {
            throw new Error();
        }
        pet.avatar = buffer;
        await pet.save();
        res.send();
    },
    (error, req, res, next) => {
        res.status(400).send("Please upload a image under 3MB");
    }
);

router.get("pets/defaultavatar",async(req,res) =>{
    res.set({'Content-Type': 'avatar/png'});

})

router.delete("/pets/:id/avatar", customerAuth, async (req, res) => {
    const pet = await Pet.findById(req.params.id);
    pet.avatar = undefined;
    await pet.save();
    res.send();
});

router.get("/pets/:id/avatar", async (req, res) => {
    try {
        const pet = await Pet.findById(req.params.id);
        if (!pet || !pet.avatar) {
            throw new Error();
        }
        res.set("Content-Type", "image/png");
        res.send(pet.avatar);
    } catch (e) {
        res.status(404).send();
    }
});

module.exports = router;
